// <== For loops  ==>

//  A for loop is used when you know how many times you want to repeat a task. It has three parts: starting point, condition, and increment/decrement.

let i = 10;
for (i = 0; i <= 10; i++) {
    console.log("Hey Coder", i);
}


// <== While loops  ==>

// A while loop runs as long as the condition is true. It checks the condition first and then executes the code.

let j = 0;

while (j <= 10) {
    console.log("Hey Coder", j);
    j++;

}

// <== Do While loops  ==>

// A do-while loop runs the code at least once and then checks the condition. If the condition is true, it keeps running the code.
let k = 0
do {
    console.log("Hey Coder", k);
    k++;
} while (k <= 20)
